	public int[] solution(int[] ascArray, int sumToSearch) throws ThereIsNoProperPair {
		int idx1 = 0;
		int idx2 = ascArray.length - 1;
		int currSum;
		while (idx1 < idx2) {
			currSum = ascArray[idx1] + ascArray[idx2];
			if (currSum < sumToSearch) {
				idx1++;
			} else if (currSum > sumToSearch) {
				idx2--;
			} else {
				return new int[]{ascArray[idx1], ascArray[idx2]};
			}
		}
		throw new ThereIsNoProperPair("There is no proper pair!");
	}
